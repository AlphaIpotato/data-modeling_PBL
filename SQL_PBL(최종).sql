-- CREATE DATABASE IF NOT EXISTS pbl_a;
use pbl_a;

CREATE TABLE 회원 (
  회원ID VARCHAR(15) NOT NULL ,
  이름 VARCHAR(25) NOT NULL,
  휴대폰번호 VARCHAR(15) NOT NULL,
  등급 VARCHAR(15) NOT NULL,
  가입일 VARCHAR(15) NOT NULL,
  PRIMARY KEY(회원ID)
);
CREATE TABLE 가게 (
  가게ID VARCHAR(15) NOT NULL ,
  가게명 VARCHAR(15) NOT NULL,
  카테고리 VARCHAR(15) NOT NULL,
  가게주소 VARCHAR(25) NOT NULL,
  가게연락처 VARCHAR(15) NOT NULL,
  최소주문금액 VARCHAR(15) NOT NULL,
  배달팁 VARCHAR(15) NOT NULL,
  운영시간 VARCHAR(25) NOT NULL,
  배달가능지역 VARCHAR(25) NOT NULL,
  평점 float(5) unsigned  NULL,
  리뷰수 int(5)  NULL,
  PRIMARY KEY(가게ID)
);
CREATE TABLE 쿠폰 (
  쿠폰ID VARCHAR(15) NOT NULL ,
  회원ID VARCHAR(15) NOT NULL,
  쿠폰명 VARCHAR(25) NOT NULL,
  쿠폰내용 VARCHAR(100) NOT NULL,
  차감금액 VARCHAR(15) NOT NULL,
  쿠폰사용조건 VARCHAR(30) NOT NULL,
  생성일 VARCHAR(15) NOT NULL,
  소멸일 VARCHAR(15) NOT NULL,
  PRIMARY KEY(쿠폰ID),
  INDEX 쿠폰_FKIndex1(회원ID),
  FOREIGN KEY(회원ID)
    REFERENCES 회원(회원ID)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION
);
CREATE TABLE 메뉴 (
  메뉴ID VARCHAR(15) NOT NULL ,
  가게ID VARCHAR(15) NOT NULL,
  메뉴이름 VARCHAR(25) NOT NULL,
  가격 VARCHAR(15) NOT NULL,
  메뉴정보 VARCHAR(50) NOT NULL,
  PRIMARY KEY(메뉴ID),
  INDEX 메뉴_FKIndex1(가게ID),
  FOREIGN KEY(가게ID)
    REFERENCES 가게(가게ID)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION
);
CREATE TABLE 리뷰 (
  리뷰ID VARCHAR(15) NOT NULL ,
  회원ID VARCHAR(15) NOT NULL,
  가게ID VARCHAR(15) NOT NULL,
  별점 float(5) unsigned NOT NULL,
  내용 VARCHAR(150) NOT NULL,
  작성일 VARCHAR(15) NOT NULL,
  PRIMARY KEY(리뷰ID),
  INDEX 리뷰_FKIndex1(가게ID),
  INDEX 리뷰_FKIndex2(회원ID),
  FOREIGN KEY(가게ID)
    REFERENCES 가게(가게ID)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION,
  FOREIGN KEY(회원ID)
    REFERENCES 회원(회원ID)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION
);
CREATE TABLE 주문이력 (
  주문ID VARCHAR(15) NOT NULL ,
  회원ID VARCHAR(15) NOT NULL,
  가게ID VARCHAR(15) NOT NULL,
  주소 VARCHAR(30) NOT NULL,
  결제수단 VARCHAR(30) NOT NULL,
  주문메뉴 VARCHAR(100) NOT NULL,
  쿠폰사용여부 VARCHAR(3) NOT NULL,
  요청사항 VARCHAR(50)  NULL,
  총액 VARCHAR(15) NOT NULL,
  할인후금액 VARCHAR(15) NULL,
  주문날짜 VARCHAR(20) NOT NULL,
  PRIMARY KEY(주문ID),
  INDEX 주문이력_FKIndex2(회원ID),
  INDEX 주문이력_FKIndex3(가게ID),
  FOREIGN KEY(회원ID)
    REFERENCES 회원(회원ID)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION,
  FOREIGN KEY(가게ID)
    REFERENCES 가게(가게ID)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION
);
CREATE TABLE 주문메뉴 (
  주문ID VARCHAR(15) NOT NULL,
  메뉴ID VARCHAR(15) NOT NULL,
  PRIMARY KEY(주문ID, 메뉴ID),
  INDEX 주문이력_has_메뉴_FKIndex1(주문ID),
  INDEX 주문이력_has_메뉴_FKIndex2(메뉴ID),
  FOREIGN KEY(주문ID)
    REFERENCES 주문이력(주문ID)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION,
  FOREIGN KEY(메뉴ID)
    REFERENCES 메뉴(메뉴ID)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION
);

